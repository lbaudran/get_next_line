#include "libft.h"

void	*ft_bzero(void *str, size_t n)
{
	char	*str1;

	str1 = str;
	if (n == 0)
		return (str);
	while (n--)
	{
		*str1 = '\0';
		str1++;
	}
	return (str1);
}
