#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		i;

	i = 0;
	if (!n)
		return (s1);
	while (s1[i] && n)
		i++;
	while (*s2 && n--)
		s1[i++] = *s2++;
	if (n)
		s1[i++] = '\0';
	return (s1);
}
