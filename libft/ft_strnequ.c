#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (!n)
		return (1);
	while (*s1 == *s2)
	{
		n--;
		if ((*s1 == *s2 && !*s1) || (!n && *s1 == *s2))
			return (1);
		s1++;
		s2++;
	}
	return (0);
}
